/**
 * Lightweight UI internationalisation for the mobile app — English + Simplified
 * Chinese. Deliberately hand-rolled (one dictionary + a context) to match the
 * web app's `apps/web/lib/i18n.tsx` and the local provider style in
 * `state/tabs.tsx`, rather than pulling in a framework.
 *
 * `t(key, params)` looks up a string for the active language, falls back to
 * English, then to the raw key, and substitutes `{name}` placeholders.
 *
 * The active language is restored from AsyncStorage after the first render: the
 * provider starts in English, then applies the stored choice in an effect. A
 * non-English user sees a brief English flash on first paint — acceptable, and
 * it keeps the provider free of a blocking load (the app shell already gates on
 * auth readiness in `App.tsx`).
 *
 * Adding a language means adding its dictionary below plus one entry in
 * `LANGUAGES` — nothing else is hardcoded.
 */
