const messages: Record<Lang, Dict> = { en, zh };

export type Translate = (key: string, params?: Record<string, string | number>) => string;

type I18n = { lang: Lang; setLang: (l: Lang) => void; t: Translate };

const I18nContext = createContext<I18n | null>(null);

export function I18nProvider({ children }: { children: React.ReactNode }) {
  const [lang, setLangState] = useState<Lang>('en');

  // Apply the stored language after the first render (see file header).
  useEffect(() => {
    AsyncStorage.getItem(STORAGE_KEY)
      .then((v) => {
        if (v && LANGUAGES.some((l) => l.code === v)) setLangState(v as Lang);
      })
      .catch(() => undefined);
  }, []);

  function setLang(next: Lang) {
    setLangState(next);
    AsyncStorage.setItem(STORAGE_KEY, next).catch(() => undefined);
  }

  const t: Translate = (key, params) => {
    let s = messages[lang][key] ?? messages.en[key] ?? key;
    if (params) {
      for (const [k, v] of Object.entries(params)) {
        s = s.split(`{${k}}`).join(String(v));
      }
    }
    return s;
  };

  return <I18nContext.Provider value={{ lang, setLang, t }}>{children}</I18nContext.Provider>;
}

/** Read the active language, the setter, and the `t()` translator. */
export function useI18n(): I18n {
  const ctx = useContext(I18nContext);
  if (!ctx) throw new Error('useI18n must be used within <I18nProvider>');
  return ctx;
}
